#!/bin/bash

for i in {6..16} 
do
	j=$((2**$i))
	echo "**************************************************************************"
	echo "Running for size $j"
	echo "**************************************************************************"
	tornado -Ds0.t0.device=0:1 uk.ac.manchester.tornado.examples.fpga.DFT $j

	echo "**************************************************************************"
	echo "Finished size $j"
	echo "**************************************************************************"
done
